// toán tử số học
console.log("TOÁN TỬ SỐ HỌC")
var num1 = 10
var num2 = 20
console.log("Toán tử cộng: ",num1 + num2);
console.log("Toán tử trừ: ",num1 - num2);
console.log("Toán tử nhân: ",num1 * num2);
console.log("Toán tử chia: ",num1 / num2);
console.log("---------------------------")

// toán tử so sánh
console.log("TOÁN TỬ SO SÁNH")
let num3 = 10
let num4 = 20
console.log("Lớn hơn: ",num3 > num4);
console.log("Lớn hơn hoặc bằng: ",num3 >= num4);
console.log("Nhỏ hơn: ",num3 < num4);
console.log("Nhỏ hơn hoặc bằng: ",num3 <= num4);
console.log("Bằng: ",num3 == num4);
console.log("---------------------------")

